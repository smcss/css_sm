    <h3 id="container">Container</h3>

    <p>Container &mdash; is a place for elements.</p>

    <p>The only responsibility of containers is to perform layout.
    They are expected to manage the following properties:</p>

    <ul>
        <li>position, top, left, right, bottom</li>
        <li>width, height</li>
        <li>margin, padding</li>
    </ul>

    <p>Only containers are responsible for managing this properties.</p>

